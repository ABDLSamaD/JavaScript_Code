// String literals
let boy1 = 'hamza'
let boy2 = "papan"
let sentence = `${boy1} is a friend of ${boy2}` //this can be a string interpolation ${boy1}
console.log(sentence);


// escape sentence character
// we can use single quotes escape sequences to solve the problem

let fruit = 'lana\'na'
console.log(fruit);

// we can use double quotes escape sequences to solve the problem
let fruits = "Mangoo\"s"
console.log(fruits);


// backslash n is create a new line \n
let fruit2 = 'apple\nis'
console.log(fruit2);

// backslash t is create a space in lines \t
let fruit3 = 'orange\tes'
console.log(fruit3);
